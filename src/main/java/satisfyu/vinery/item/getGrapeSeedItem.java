package satisfyu.vinery.item;

import net.minecraft.entity.LivingEntity;
import net.minecraft.entity.player.PlayerEntity;
import net.minecraft.item.Item;
import net.minecraft.item.ItemStack;
import net.minecraft.world.World;
import satisfyu.vinery.registry.ObjectRegistry;

public class getGrapeSeedItem extends Item {
    public getGrapeSeedItem(Item.Settings settings) {
        super(settings);
    }

    public ItemStack finishUsing(ItemStack stack, World world, LivingEntity user) {
        ItemStack itemStack = super.finishUsing(stack, world, user);
        return user instanceof PlayerEntity && ((PlayerEntity)user).getAbilities().creativeMode ? itemStack : new ItemStack(ObjectRegistry.JUNGLE_RED_GRAPE_SEEDS);
    }
}

